package aprove.verification.oldframework.Bytecode.Processors;

public class DumpFailedException extends Exception {

    public DumpFailedException() {
        super();
    }

    public DumpFailedException(String message, Throwable cause) {
        super(message, cause);
    }

    public DumpFailedException(String message) {
        super(message);
    }

    public DumpFailedException(Throwable cause) {
        super(cause);
    }
}
